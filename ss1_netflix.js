function drop1() {
    var cont = document.getElementById('fifthboxjs');
    if (cont.style.display == 'block') {
        cont.style.display = 'none';

        document.getElementById(fifthboxjs).value = 'Hide DIV';
    }
    else {
        cont.style.display = 'block';
        document.getElementById(fifthboxjs).value = 'Show DIV';
    }
}
